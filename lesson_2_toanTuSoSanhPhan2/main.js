/** toán tử so sánh phần 2 (so sánh tuyệt đối)
 * === (bằng tuyệt đối)
 * !== (khác tuyệt đối)
 */

var a = 1;
var b = '1';
console.log(a == b); // chỉ so sánh value
console.log(a === b); // so sánh cả value lẫn kiểu dữ liệu
console.log(a != b); // chỉ so sánh value
console.log(a !== b); // so sánh cả value lẫn kiểu dữ liệu