/**
 toán tử         ví dụ       tương đương
    ++          x ++         x = x + 1 // tăng gtri biến lên 1 và trả về gtri trước khi tăng
    ++          ++ x         x = x + 1 // tăng gtri biến lên 1 và trả về gtri sau khi tăng
    --          x --         x = x - 1
    --          -- x         x = x - 1
 */

var a = 1;
console.log('a = ', a++);
console.log('a = ', a);

var b = 1;
console.log('b = ', ++b);
console.log('b = ', b);
    
    
    
    
    
    
    
